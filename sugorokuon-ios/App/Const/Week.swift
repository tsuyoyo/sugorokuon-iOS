import Foundation

/**
 * Value is following what Calendar.component(.weekday, date) returns.
 *
 */
enum Week : Int {
    case Sunday = 1
    case Monday = 2
    case Tuesday = 3
    case Wednesday = 4
    case Thursday = 5
    case Friday = 6
    case Saturday = 7
}
